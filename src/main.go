package main

import "fmt"

func main() {

	// Constants declaration
	const pi float64 = 3.14
	const pi2 = 3.1415
	fmt.Println("pi:", pi)
	fmt.Println("pi2:", pi2)

	// Integer declaration
	var altura int = 10
	var base int
	altura2 := 20
	fmt.Println(altura, base, altura2)

	// Zero values
	var a int
	var b float64
	var c string
	var d bool
	fmt.Println(a, b, c, d)

}
